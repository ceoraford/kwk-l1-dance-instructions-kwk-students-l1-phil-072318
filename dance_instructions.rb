
# Code your methods below
def assume_stance 
  puts "Plant legs far apart, bend knees slightly and keep posture loose"
end
def footwork
  puts "Lift right foot"
  puts "Return right foot to the ground"
  puts "Finishing with a small skip-step backward"
end 
def skip_steps
  puts "Lower left foot to the ground"
  puts "Bounce left foot back up slightly, kicking a few inches back"
  puts "Left, Right, Left and Left"
  puts "Lower right foot to the ground"
  puts "Bounce right foot back up slightly, kicking a few inches back"
  puts "Right, Left, Right and Right"
end
def bob_the_reins
  puts "Holding your arms out in front of you, straight and at chest level"
  puts "Cross your right wrist over your left and hold them together"
  puts " Lift your arms up and down in a loose bouncing movement, in time with the beat of the song"
end
def lasso 
  puts "Start arm movement by holding left arm so the back of your wrist is near your chin, pointing yourleft elbow straight left."
  puts "Lift right arm up and point right elbow diagonally to the right."
  puts "Raise right forearm so that it points straight up, and whip it in small circles to the beat of the song, as if you were a cowboy holding a rope lasso."
end 
def bust_a_move
  puts "Be cool"
  puts "Be snazzy!"
end

def gangnam_style
  assume_stance
  bust_a_move
  footwork
  bob_the_reins + skip_steps
  lasso + skip_steps
end 


